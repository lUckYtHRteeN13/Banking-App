    public void switchFrames(javax.swing.JPanel firstFrame, javax.swing.JPanel secondFrame) {
        if (secondFrame != null) {
            firstFrame.setVisible(false);        
            secondFrame.setVisible(true);

            for (java.awt.Component comp : firstFrame.getComponents()) {
                if (comp instanceof javax.swing.JTextField) {
                    ((javax.swing.JTextField) comp).setText("");
                }
            }
        }
    };
    
    public void addBalance(double val) {
        this.setBalance(this.getBalance() +  val);
        JOptionPane.showMessageDialog(this, String.format(
                "Deposit Amount: %.2f Bytes\n"
                + "------------------------------------\n"
                + "Current Balance: %.2f Bytes", val, this.getBalance()),
                "Success",
                1);        
    }
    
    public void deduceBalance(double val) {
        double totalAmount = this.getBalance() -  val;
        
        if (totalAmount >= 0) {
            this.setBalance(totalAmount);
            JOptionPane.showMessageDialog(this, String.format(
                    "Withdraw Amount: %.2f Bytes\n"
                    + "------------------------------------\n"
                    + "Remaining Balance: %.2f Bytes", val, this.getBalance()),
                    "Success",
                    1);
        } else {
            JOptionPane.showMessageDialog(this,
                    "INSUFICIENT FUNDS!",
                    "Insuficient",
                    1);
        }
        
    }
    
